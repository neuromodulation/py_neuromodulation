from torch import nn
import torch
from torch.optim.optimizer import Optimizer
import numpy as np
class Attention(nn.Module):
    def __init__(self, feature_dim, step_dim, bias=True, **kwargs):
        super(Attention, self).__init__(**kwargs)

        self.supports_masking = True

        self.bias = bias
        self.feature_dim = feature_dim
        self.step_dim = step_dim
        self.features_dim = 0

        weight = torch.zeros(feature_dim, 1)
        nn.init.xavier_uniform_(weight)
        self.weight = nn.Parameter(weight)

        if bias:
            self.b = nn.Parameter(torch.zeros(step_dim))

    def forward(self, x, mask=None):
        feature_dim = self.feature_dim
        step_dim = self.step_dim

        eij = torch.mm(
            x.contiguous().view(-1, feature_dim),
            self.weight
        ).view(-1, step_dim)

        if self.bias:
            eij = eij + self.b

        eij = torch.tanh(eij)
        a = torch.exp(eij)

        if mask is not None:
            a = a * mask

        a = a / torch.sum(a, 1, keepdim=True) + 1e-10

        weighted_input = x * torch.unsqueeze(a, -1)
        return torch.sum(weighted_input, 1)

class AttentionWithContext(nn.Module):
    def __init__(self, feature_dim, step_dim, bias=True, **kwargs):
        super(AttentionWithContext, self).__init__(**kwargs)

        self.supports_masking = True

        self.bias = bias
        self.feature_dim = feature_dim
        self.step_dim = step_dim
        self.features_dim = 0

        weight = torch.zeros(feature_dim, feature_dim,1)
        weight = nn.init.xavier_uniform_(weight)
        self.weight = nn.Parameter(weight)

        context = torch.zeros(feature_dim,1)
        context = nn.init.xavier_uniform_(context)
        self.context = nn.Parameter(context)

        if bias:
            self.b = nn.Parameter(torch.zeros(feature_dim))

    def forward(self, x, mask=None):
        feature_dim = self.feature_dim
        step_dim = self.step_dim

        uit = torch.matmul(
            x.contiguous().view(-1, feature_dim),
            self.weight
        ).view(-1, feature_dim)

        if self.bias:
            uit = uit + self.b

        uit = torch.tanh(uit)
        ait = torch.matmul(uit.view(-1, feature_dim), self.context).view(-1, step_dim)

        a = torch.exp(ait)

        if mask is not None:
            a = a * mask

        a = a / torch.sum(a, 1, keepdim=True) + 1e-10

        weighted_input = x * torch.unsqueeze(a, -1)
        return torch.sum(weighted_input, 1)

